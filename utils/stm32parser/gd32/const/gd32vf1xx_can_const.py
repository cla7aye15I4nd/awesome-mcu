from enum import IntEnum


class CTL(IntEnum):
    DFZ     = 0x1 << 16   # Debug freeze
    SWRST   = 0x1 << 15   # Software reset
    TTC     = 0x1 << 7    # Time-triggered communication
    ABOR    = 0x1 << 6    # Automatic bus-off recovery
    AWU     = 0x1 << 5    # Automatic wakeup
    ARD     = 0x1 << 4    # Automatic retransmission disable
    RFOD    = 0x1 << 3    # Receive FIFO overwrite disable
    TFO     = 0x1 << 2    # Transmit FIFO order
    SLPWMOD = 0x1 << 1    # Sleep working mode
    IWMOD   = 0x1 << 0    # Initial working mode

class STAT(IntEnum):
    RXL    = 0x1 << 11   # RX level
    LASTRX = 0x1 << 10   # Last sample value of RX pin
    RS     = 0x1 << 9    # Receiving state
    TS     = 0x1 << 8    # Transmitting state
    SLPIF  = 0x1 << 4    # Status change interrupt flag of sleep working mode entering
    WUIF   = 0x1 << 3    # Status change interrupt flag of wakeup from sleep working mode
    ERRIF  = 0x1 << 2    # Error interrupt flag
    SLPWS  = 0x1 << 1    # Sleep working state
    IWS    = 0x1 << 0    # Initial working state

class TSTAT(IntEnum):
    TMLS2    = 0x1 << 31   # Transmit mailbox 2 last sending in transmit FIFO
    TMLS1    = 0x1 << 30   # Transmit mailbox 1 last sending in transmit FIFO
    TMLS0    = 0x1 << 29   # Transmit mailbox 0 last sending in transmit FIFO
    TME2     = 0x1 << 28   # Transmit mailbox 2 empty
    TME1     = 0x1 << 27   # Transmit mailbox 1 empty
    TME0     = 0x1 << 26   # Transmit mailbox 0 empty
    NUM      = 0x3 << 24   # number of the transmit FIFO mailbox in which the frame will be transmitted if at least one mailbox is empty
    MST2     = 0x1 << 23   # Mailbox 2 stop transmitting
    MTE2     = 0x1 << 19   # Mailbox 2 transmit error
    MAL2     = 0x1 << 18   # Mailbox 2 arbitration lost
    MTFNERR2 = 0x1 << 17   # Mailbox 2 transmit finished and no error
    MTF2     = 0x1 << 16   # Mailbox 2 transmit finished
    MST1     = 0x1 << 15   # Mailbox 1 stop transmitting
    MTE1     = 0x1 << 11   # Mailbox 1 transmit error
    MAL1     = 0x1 << 10   # Mailbox 1 arbitration lost
    MTFNERR1 = 0x1 << 9    # Mailbox 1 transmit finished and no error
    MTF1     = 0x1 << 8    # Mailbox 1 transmit finished
    MST0     = 0x1 << 7    # Mailbox 0 stop transmitting
    MTE0     = 0x1 << 3    # Mailbox 0 transmit error
    MAL0     = 0x1 << 2    # Mailbox 0 arbitration lost
    MTFNERR0 = 0x1 << 1    # Mailbox 0 transmit finished and no error
    MTF0     = 0x1 << 0    # Mailbox 0 transmit finished

